const fs = require('fs');
const path = require('path');
const matter = require('gray-matter');
const MarkdownIt = require('markdown-it');

const markdownRenderer = new MarkdownIt({
  html: false,
  linkify: true,
  typographer: false,
  breaks: false
});

function normalizeMarkdown(markdown = '') {
  return String(markdown).replace(/\\[ \t]*(\r?\n)/g, '$1');
}

function markdownToHtml(markdown = '') {
  return markdownRenderer.render(normalizeMarkdown(markdown));
}

function formatDateToHebrew(dateString) {
  try {
    const date = new Date(dateString);

    return new Intl.DateTimeFormat('he-IL', {
      day: 'numeric',
      month: 'long',
      year: 'numeric'
    }).format(date);
  } catch (error) {
    console.error('Error formatting date:', error);
    return dateString;
  }
}

function extractSlugFromFilename(fileName) {
  const match = fileName.match(/^\d{4}-\d{2}-\d{2}-(.+)\.md$/);

  if (match && match[1]) {
    return match[1];
  }

  return fileName.replace(/\.md$/, '');
}

function escapeJsString(value = '') {
  return String(value)
    .replace(/\\/g, '\\\\')
    .replace(/"/g, '\\"')
    .replace(/\r?\n/g, '\\n');
}

function escapeTemplateLiteral(value = '') {
  return String(value)
    .replace(/\\/g, '\\\\')
    .replace(/`/g, '\\`')
    .replace(/\$\{/g, '\\${');
}

function getAllBlogPosts() {
  const blogDir = path.join(__dirname, '..', 'src', 'content', 'blog');
  const fileNames = fs.readdirSync(blogDir);

  let id = 1;

  const allPostsData = fileNames
    .filter(fileName => fileName.endsWith('.md'))
    .map(fileName => {
      const slug = extractSlugFromFilename(fileName);
      const fullPath = path.join(blogDir, fileName);
      const fileContents = fs.readFileSync(fullPath, 'utf8');
      const matterResult = matter(fileContents);
      const rawDate = matterResult.data.date;

      return {
        id: id++,
        title: matterResult.data.title || 'ללא כותרת',
        slug,
        rawDate,
        date: formatDateToHebrew(rawDate),
        excerpt: matterResult.data.excerpt || '',
        image: matterResult.data.image || '',
        categories: matterResult.data.categories || [],
        content: markdownToHtml(matterResult.content)
      };
    });

  return allPostsData.sort((a, b) => new Date(b.rawDate) - new Date(a.rawDate));
}

function updateBlogPostsData() {
  const posts = getAllBlogPosts();

  const indexContent = `// This file is generated by scripts/update-blog-data.js.
// Do not edit it manually. Run npm run update-blog instead.

${posts.map(post => `import post${post.id} from './post${post.id}';`).join('\n')}

const blogPosts = [
  ${posts.map(post => `post${post.id}`).join(',\n  ')}
];

export default blogPosts;`;

  const indexPath = path.join(__dirname, '..', 'src', 'data', 'blogPostsData', 'index.js');
  fs.writeFileSync(indexPath, indexContent);

  posts.forEach(post => {
    const postContent = `// This file is generated by scripts/update-blog-data.js.
// Do not edit it manually. Run npm run update-blog instead.

const post${post.id} = {
  id: ${post.id},
  title: "${escapeJsString(post.title)}",
  slug: "${escapeJsString(post.slug)}",
  date: "${escapeJsString(post.date)}",
  excerpt: "${escapeJsString(post.excerpt)}",
  image: "${escapeJsString(post.image)}",
  categories: [${post.categories.map(cat => `"${escapeJsString(cat)}"`).join(', ')}],
  content: \`${escapeTemplateLiteral(post.content)}\`
};

export default post${post.id};`;

    const postPath = path.join(__dirname, '..', 'src', 'data', 'blogPostsData', `post${post.id}.js`);
    fs.writeFileSync(postPath, postContent);
  });

  console.log(`Updated ${posts.length} blog posts successfully.`);
}

updateBlogPostsData();
